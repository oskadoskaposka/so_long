#include "../../libft.h"

int	ft_find(const char *str, char c)
{
	int	i;

	i = 0;
	if (!*str || !c)
		return (0);
	while (*str != c)
	{
		if (*str == '\0')
			return (0);
		str++;
		i++;
	}
	return (i);
}
